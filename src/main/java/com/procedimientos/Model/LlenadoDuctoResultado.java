package com.procedimientos.Model;

public class LlenadoDuctoResultado {
    private double areaDucto;
    private double areaFase;
    private double areaNeutro;
    private double areaTierra;
    private double areaTotal;
    private double llenado;

    // Getters y Setters
    public double getAreaDucto() { return areaDucto; }
    public void setAreaDucto(double areaDucto) { this.areaDucto = areaDucto; }

    public double getAreaFase() { return areaFase; }
    public void setAreaFase(double areaFase) { this.areaFase = areaFase; }

    public double getAreaNeutro() { return areaNeutro; }
    public void setAreaNeutro(double areaNeutro) { this.areaNeutro = areaNeutro; }

    public double getAreaTierra() { return areaTierra; }
    public void setAreaTierra(double areaTierra) { this.areaTierra = areaTierra; }

    public double getAreaTotal() { return areaTotal; }
    public void setAreaTotal(double areaTotal) { this.areaTotal = areaTotal; }

    public double getLlenado() { return llenado; }
    public void setLlenado(double llenado) { this.llenado = llenado; }
}
